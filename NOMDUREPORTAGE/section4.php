<h3>Le futur du Bitcoin plus qu’incertain</h3>
<br/>
<p>
Les plus rationnels d’entre nous ont annoncé sa mort il y a bien longtemps, tandis que d’autres restent confiants et continuent de croire en cette technologie.
Bruno Colmant admet qu’il a jugé la monnaie trop vite : « Il y a un an je considérais le Bitcoin comme le « bitcon » mais j’avoue que je me suis trompé. C’est bien plus innovateur qu’on ne le pense. Finalement, même si le Bitcoin est un échec, il aura eu le mérite d’intéresser beaucoup de gens et de susciter un intérêt technologique qui peut-être n’aurait pas existé sans le Bitcoin ». 
</p>
<p>
Bien qu’il soit difficilement concevable qu’elle devienne une monnaie universellement admise, cette avancée technologique offre une alternative aux personnes fuyant le système bancaire classique mais ce n’est pas tout. Au-delà des spéculations et des parties sombres qui entourent cette monnaie,
il convient de la voir comme le prolongement logique de l’expansion d’Internet, un territoire virtuel sans frontière et autrefois sans loi dans lequel une frange de la population se reconnaît et défie l’ordre établi. Et ça, c’est certain.
</p>
