<h4>Chapeau
</h4>
<br/>
Aujourd’hui, des plateformes comme <a href="https://btc-e.com" target="_blank" title="BTC-E">BTC-E</a> ou <a href="https://www.bitstamp.net/" target="_blank" title="bitstamp">Bitstamp</a> permettent à ceux qui l’utilisent, d’échanger leurs Bitcoins contre des devises classiques ou virtuelles.
Si le Bitcoin bénéficie d’une couverture médiatique que lui envient tous ses concurrents, il est aussi celui qui ouvre la voie aux autres crypto-devises. 
Parmi les milliers d’autres monnaies apparues sur le marché, on peut citer par exemple le LiteCoin, le DarkCoin ou le CannabisCoin. Toutes fluctuent énormément sur le marché et leurs valeurs dépassent rarement le centime d’euro.
Si la majorité d’entre elles vont probablement disparaître dans les mois qui viennent pour être remplacées par d’autres, il se pourrait que certaines sortent du lot et s’imposent comme alternative au Bitcoin, voire même le détrônent.
